// Isolated repository fixtures for the deterministic Release Preparation
// transaction (ADR 0003).
//
// Every fixture builds one or more fresh temporary Git repositories from the
// repository's real .changie.yaml and baseline records, then drives
// `prepareRelease` — the exact transaction behind `pnpm release:prepare` —
// with the real Changie binary. Fixtures prove automatic and explicit version
// selection, fragment consumption into exactly one Released Version Notes
// record, package-version agreement, byte-for-byte changelog regeneration,
// rejection of extra repository changes, and that failures preserve partial
// output instead of rolling back. Nothing here commits to, tags, pushes, or
// publishes anything, and the repository itself is never touched.

import { spawnSync } from 'node:child_process';
import {
  appendFileSync,
  copyFileSync,
  existsSync,
  mkdirSync,
  mkdtempSync,
  readFileSync,
  rmSync,
  unlinkSync,
  writeFileSync,
} from 'node:fs';
import { tmpdir } from 'node:os';
import { dirname, join } from 'node:path';
import { listUnreleasedFragments } from './change-fragments.mjs';
import {
  collectChangelogContractErrors,
  regenerateReleaseChangelog,
  resolveChangieBinary,
} from './release-changelog.mjs';
import { prepareRelease } from './release-preparation.mjs';

const changieTimeoutMs = 30_000;

const fixtures = [
  { name: 'automatic selection prepares a minor pre-1.0 candidate', run: fixtureAutomaticMinorCandidate },
  { name: 'automatic selection prepares a patch candidate', run: fixtureAutomaticPatchCandidate },
  {
    name: 'automatic selection never infers a release stage promotion',
    run: fixtureAutomaticSelectionNeverPromotes,
  },
  { name: 'an explicit version prepares a prerelease candidate', run: fixtureExplicitPrereleaseCandidate },
  { name: 'an explicit version may promote deliberately', run: fixtureExplicitDeliberatePromotion },
  { name: 'invalid explicit versions fail before consuming fragments', run: fixtureInvalidExplicitVersions },
  { name: 'preparation requires pending fragments', run: fixtureRequiresPendingFragments },
  { name: 'extra repository changes fail the candidate contract', run: fixtureRejectsExtraChanges },
  { name: 'package and record version disagreement fails', run: fixtureVersionDisagreementFails },
  { name: 'a tampered candidate fails re-validation', run: fixtureTamperedCandidateFailsRevalidation },
  { name: 'a failed merge preserves partial output for inspection', run: fixtureFailedMergePreservesOutput },
];

export function runReleasePreparationFixtures({ root }) {
  const binary = resolveChangieBinary(root);
  if (!existsSync(binary)) {
    return {
      failures: [`Missing Changie binary at ${binary}; run pnpm install first.`],
      total: fixtures.length,
    };
  }

  const failures = [];
  for (const fixture of fixtures) {
    for (const failure of runFixture(root, binary, fixture)) {
      failures.push(`release-preparation fixture "${fixture.name}": ${failure}`);
    }
  }

  return { failures, total: fixtures.length };
}

function runFixture(root, binary, fixture) {
  const parent = mkdtempSync(join(tmpdir(), 'hell-release-preparation-'));
  const failures = [];
  let workspaceCount = 0;
  try {
    const context = {
      failures,
      fail: (message) => failures.push(message),
      workspace: (options = {}) => {
        const dir = join(parent, `workspace-${workspaceCount++}`);
        return createWorkspaceContext(root, binary, dir, failures, options);
      },
    };
    fixture.run(context);
    return failures;
  } catch (error) {
    return [...failures, error instanceof Error ? error.message : String(error)];
  } finally {
    rmSync(parent, { force: true, recursive: true });
  }
}

// Builds one temporary Git repository shaped like this repository's
// release-note surface: the real Changie configuration and 0.2.0 baseline,
// optional extra released records, a minimal published package manifest, the
// given pending fragments, and a CHANGELOG generated by the real
// `changie merge` — all committed as the baseline.
function createWorkspaceContext(root, binary, dir, failures, options) {
  const {
    manifestVersion = '0.2.0',
    records = {},
    fragments = {},
    files = {},
    omitHeader = false,
  } = options;

  const unreleasedDir = join(dir, '.changes', 'unreleased');
  mkdirSync(unreleasedDir, { recursive: true });
  mkdirSync(join(dir, 'packages', 'angular'), { recursive: true });
  copyFileSync(join(root, '.changie.yaml'), join(dir, '.changie.yaml'));
  copyFileSync(join(root, '.changes', 'header.tpl.md'), join(dir, '.changes', 'header.tpl.md'));
  copyFileSync(join(root, '.changes', '0.2.0.md'), join(dir, '.changes', '0.2.0.md'));
  for (const [name, content] of Object.entries(records)) {
    writeFileSync(join(dir, '.changes', name), content);
  }
  writeFileSync(
    join(dir, 'packages', 'angular', 'package.json'),
    `{\n  "name": "hell-ui",\n  "version": "${manifestVersion}"\n}\n`,
  );
  for (const [path, content] of Object.entries(files)) {
    mkdirSync(dirname(join(dir, path)), { recursive: true });
    writeFileSync(join(dir, path), content);
  }
  for (const [name, content] of Object.entries(fragments)) {
    writeFileSync(join(unreleasedDir, name), content);
  }
  writeFileSync(join(unreleasedDir, '.gitkeep'), '');

  const merge = spawnSync(binary, ['merge'], {
    cwd: dir,
    encoding: 'utf8',
    killSignal: 'SIGKILL',
    timeout: changieTimeoutMs,
  });
  if (merge.status !== 0) {
    throw new Error(`fixture baseline changie merge failed: ${merge.stderr || merge.stdout}`);
  }
  if (omitHeader) unlinkSync(join(dir, '.changes', 'header.tpl.md'));

  const git = (args) => spawnSync('git', args, { cwd: dir, encoding: 'utf8' });
  const gitOrThrow = (args) => {
    const result = git(args);
    if (result.status !== 0) {
      throw new Error(`git ${args.join(' ')} failed: ${result.stderr || result.stdout}`);
    }
    return result;
  };
  gitOrThrow(['init', '--quiet']);
  gitOrThrow(['add', '--all']);
  gitOrThrow([
    '-c',
    'user.name=hell-fixture',
    '-c',
    'user.email=fixture@example.invalid',
    '-c',
    'commit.gpgsign=false',
    'commit',
    '--quiet',
    '--message',
    'baseline',
  ]);

  return {
    dir,
    fail: (message) => failures.push(message),
    prepare: (explicitVersion = null) =>
      prepareRelease({ root: dir, changieBinary: binary, explicitVersion }),
    exists: (path) => existsSync(join(dir, path)),
    read: (path) => readFileSync(join(dir, path), 'utf8'),
    write: (path, content) => writeFileSync(join(dir, path), content),
    append: (path, content) => appendFileSync(join(dir, path), content),
    manifestVersion: () =>
      JSON.parse(readFileSync(join(dir, 'packages', 'angular', 'package.json'), 'utf8')).version,
    pendingFragments: () => listUnreleasedFragments(unreleasedDir),
    statusEntries: () =>
      gitOrThrow(['status', '--porcelain']).stdout.split('\n').filter(Boolean).sort(),
    contractErrors: () =>
      collectChangelogContractErrors({ root: dir, changieBinary: binary }).errors,
    regenerate: () => regenerateReleaseChangelog({ root: dir, changieBinary: binary }),
  };
}

function fragment(kind, body, time) {
  return `kind: ${kind}\nbody: ${body}\ntime: ${time}\n`;
}

function breakingFragment(body, migration, time) {
  return `kind: Breaking\nbody: ${body}\ntime: ${time}\ncustom:\n  migration: ${migration}\n`;
}

function releasedRecord(version, date, body) {
  return `## [${version}] - ${date}\n\n### Fixed\n\n- ${body}\n`;
}

function expectSuccess(context, result, label) {
  for (const failure of result.failures) {
    context.fail(`${label} unexpectedly failed: ${failure}`);
  }
  return result.failures.length === 0;
}

function expectFailure(context, result, needle, label) {
  if (!result.failures.some((failure) => failure.includes(needle))) {
    context.fail(
      `${label} should fail mentioning "${needle}"; got: ${result.failures.join(' | ') || '(success)'}`,
    );
  }
}

function expectUntouchedBaseline(context, workspace, label) {
  const status = workspace.statusEntries();
  if (status.length > 0) {
    context.fail(`${label} must not touch the repository; git status reported: ${status.join(' | ')}`);
  }
}

function fixtureAutomaticMinorCandidate(context) {
  const workspace = context.workspace({
    fragments: {
      'Breaking-20260724-100001.yaml': breakingFragment(
        'Renamed the widget input.',
        'Rename hellWidget to hellGadget in consumer templates.',
        '2026-07-24T10:00:01.000Z',
      ),
      'Added-20260724-100002.yaml': fragment(
        'Added',
        'Added the pagination control.',
        '2026-07-24T10:00:02.000Z',
      ),
      'Fixed-20260724-100003.yaml': fragment(
        'Fixed',
        'Fixed toast exit ordering.',
        '2026-07-24T10:00:03.000Z',
      ),
    },
  });

  const result = workspace.prepare();
  if (!expectSuccess(context, result, 'automatic minor preparation')) return;
  if (result.version !== '0.3.0') {
    context.fail(`Breaking and Added fragments must select 0.3.0; selected ${result.version}.`);
    return;
  }

  const record = workspace.read('.changes/0.3.0.md');
  if (!/^## \[0\.3\.0\] - \d{4}-\d{2}-\d{2}\n\n### Breaking changes\n\n- /.test(record)) {
    context.fail(`the batched record must render heading and spaced kind sections; got:\n${record}`);
  }
  if (!record.includes('\n\n  Migration: Rename hellWidget')) {
    context.fail('the batched record must carry the Breaking migration paragraph.');
  }
  if (!record.includes('\n\n### Added\n\n- Added the pagination control.')) {
    context.fail('the batched record must render the Added section with blank-line spacing.');
  }
  if (!record.endsWith('- Fixed toast exit ordering.\n')) {
    context.fail('the batched record must end with the last change and one trailing newline.');
  }

  if (workspace.manifestVersion() !== '0.3.0') {
    context.fail(`the package manifest must carry 0.3.0; found ${workspace.manifestVersion()}.`);
  }
  if (workspace.pendingFragments().length !== 0) {
    context.fail(`all pending fragments must be consumed; left ${workspace.pendingFragments().join(', ')}.`);
  }
  if (!workspace.exists('.changes/unreleased')) {
    context.fail('the unreleased directory must survive preparation.');
  }

  const changelog = workspace.read('CHANGELOG.md');
  if (!changelog.startsWith('# Changelog\n\n## [0.3.0] - ')) {
    context.fail('the regenerated changelog must lead with the new release.');
  }
  if (!changelog.includes('.\n\n## [0.2.0] - 2026-07-03\n')) {
    context.fail('the regenerated changelog must keep one blank line between releases.');
  }

  const expectedStatus = [
    ' D .changes/unreleased/Added-20260724-100002.yaml',
    ' D .changes/unreleased/Breaking-20260724-100001.yaml',
    ' D .changes/unreleased/Fixed-20260724-100003.yaml',
    ' M CHANGELOG.md',
    ' M packages/angular/package.json',
    '?? .changes/0.3.0.md',
  ];
  const status = workspace.statusEntries();
  if (status.join('\n') !== expectedStatus.join('\n')) {
    context.fail(
      `one preparation must change exactly the allowed artifact set; git status reported: ${status.join(' | ')}`,
    );
  }

  const regenerated = workspace.regenerate();
  if (regenerated.failures.length > 0) {
    context.fail(`regenerating the prepared changelog failed: ${regenerated.failures.join(' | ')}`);
  } else if (regenerated.content !== changelog) {
    context.fail('regenerating the prepared changelog again must reproduce it byte-for-byte.');
  }

  const contractErrors = workspace.contractErrors();
  if (contractErrors.length > 0) {
    context.fail(`the prepared candidate must satisfy the changelog contract: ${contractErrors.join(' | ')}`);
  }
}

function fixtureAutomaticPatchCandidate(context) {
  const workspace = context.workspace({
    fragments: {
      'Changed-20260724-100001.yaml': fragment(
        'Changed',
        'Reworked toast stacking without behavior changes.',
        '2026-07-24T10:00:01.000Z',
      ),
      'Fixed-20260724-100002.yaml': fragment(
        'Fixed',
        'Fixed combobox filtering.',
        '2026-07-24T10:00:02.000Z',
      ),
      'Security-20260724-100003.yaml': fragment(
        'Security',
        'Hardened the file picker against path traversal.',
        '2026-07-24T10:00:03.000Z',
      ),
    },
  });

  const result = workspace.prepare();
  if (!expectSuccess(context, result, 'automatic patch preparation')) return;
  if (result.version !== '0.2.1') {
    context.fail(`Changed, Fixed, and Security fragments must select 0.2.1; selected ${result.version}.`);
    return;
  }
  if (!workspace.exists('.changes/0.2.1.md')) {
    context.fail('the patch candidate must create .changes/0.2.1.md.');
  }
  if (workspace.manifestVersion() !== '0.2.1') {
    context.fail(`the package manifest must carry 0.2.1; found ${workspace.manifestVersion()}.`);
  }
}

function fixtureAutomaticSelectionNeverPromotes(context) {
  const nearStable = context.workspace({
    manifestVersion: '0.9.9',
    records: { '0.9.9.md': releasedRecord('0.9.9', '2026-07-20', 'Earlier consumer fix.') },
    fragments: {
      'Breaking-20260724-100001.yaml': breakingFragment(
        'Removed the deprecated tuple alias.',
        'Import the HELL_ALERT_IMPORTS tuple instead.',
        '2026-07-24T10:00:01.000Z',
      ),
    },
  });
  const nearStableResult = nearStable.prepare();
  if (expectSuccess(context, nearStableResult, 'preparation from 0.9.9')) {
    if (nearStableResult.version !== '0.10.0') {
      context.fail(
        `a Breaking fragment on 0.9.9 must select 0.10.0, never 1.0.0; selected ${nearStableResult.version}.`,
      );
    }
  }

  const stable = context.workspace({
    manifestVersion: '1.2.3',
    records: { '1.2.3.md': releasedRecord('1.2.3', '2026-07-20', 'Stable-era fix.') },
    fragments: {
      'Fixed-20260724-100001.yaml': fragment('Fixed', 'Fixed a stable-era bug.', '2026-07-24T10:00:01.000Z'),
    },
  });
  expectFailure(context, stable.prepare(), 'pass an explicit version', 'automatic selection after 1.0.0');
  expectUntouchedBaseline(context, stable, 'refused automatic selection after 1.0.0');

  const prerelease = context.workspace({
    manifestVersion: '0.3.0-beta.1',
    records: { '0.3.0-beta.1.md': releasedRecord('0.3.0-beta.1', '2026-07-21', 'Beta fix.') },
    fragments: {
      'Fixed-20260724-100001.yaml': fragment('Fixed', 'Fixed a beta bug.', '2026-07-24T10:00:01.000Z'),
    },
  });
  expectFailure(context, prerelease.prepare(), 'pass an explicit version', 'automatic selection on a prerelease');
  expectUntouchedBaseline(context, prerelease, 'refused automatic selection on a prerelease');
}

function fixtureExplicitPrereleaseCandidate(context) {
  const workspace = context.workspace({
    fragments: {
      'Fixed-20260724-100001.yaml': fragment('Fixed', 'Fixed toast exit ordering.', '2026-07-24T10:00:01.000Z'),
    },
  });

  const result = workspace.prepare('0.3.0-beta.1');
  if (!expectSuccess(context, result, 'explicit prerelease preparation')) return;
  if (result.version !== '0.3.0-beta.1') {
    context.fail(`explicit preparation must use the requested version; selected ${result.version}.`);
  }
  if (!workspace.exists('.changes/0.3.0-beta.1.md')) {
    context.fail('the prerelease candidate must create .changes/0.3.0-beta.1.md.');
  }
  if (workspace.manifestVersion() !== '0.3.0-beta.1') {
    context.fail(`the package manifest must carry 0.3.0-beta.1; found ${workspace.manifestVersion()}.`);
  }
}

function fixtureExplicitDeliberatePromotion(context) {
  const workspace = context.workspace({
    fragments: {
      'Breaking-20260724-100001.yaml': breakingFragment(
        'Adopted the stable API surface.',
        'Follow the 1.0 migration guide for renamed entry points.',
        '2026-07-24T10:00:01.000Z',
      ),
    },
  });

  const result = workspace.prepare('1.0.0');
  if (!expectSuccess(context, result, 'explicit 1.0.0 preparation')) return;
  if (result.version !== '1.0.0' || !workspace.exists('.changes/1.0.0.md')) {
    context.fail('an explicit 1.0.0 must be honored as a deliberate Release Stage Promotion.');
  }
}

function fixtureInvalidExplicitVersions(context) {
  const workspace = context.workspace({
    fragments: {
      'Fixed-20260724-100001.yaml': fragment('Fixed', 'Fixed toast exit ordering.', '2026-07-24T10:00:01.000Z'),
    },
  });

  expectFailure(context, workspace.prepare('not-a-version'), 'must be valid SemVer', 'a malformed version');
  expectFailure(context, workspace.prepare('0.2.0'), 'must advance the latest released version', 're-preparing 0.2.0');
  expectFailure(context, workspace.prepare('0.1.9'), 'must advance the latest released version', 'a regressive version');
  expectUntouchedBaseline(context, workspace, 'a rejected explicit version');
  if (workspace.pendingFragments().length !== 1) {
    context.fail('rejected explicit versions must not consume pending fragments.');
  }
}

function fixtureRequiresPendingFragments(context) {
  const workspace = context.workspace();
  expectFailure(context, workspace.prepare(), 'No pending Change Fragments', 'preparing without fragments');
  if (workspace.exists('.changes/0.2.1.md') || workspace.exists('.changes/0.3.0.md')) {
    context.fail('preparation without fragments must not fabricate a version record.');
  }
  expectUntouchedBaseline(context, workspace, 'preparation without fragments');
}

function fixtureRejectsExtraChanges(context) {
  const dirtySource = context.workspace({
    files: { 'packages/angular/button/button.ts': 'export const button = true;\n' },
    fragments: {
      'Fixed-20260724-100001.yaml': fragment('Fixed', 'Fixed toast exit ordering.', '2026-07-24T10:00:01.000Z'),
    },
  });
  dirtySource.write('packages/angular/button/button.ts', 'export const button = false;\n');
  expectFailure(
    context,
    dirtySource.prepare(),
    'outside the Release Preparation contract',
    'preparing over a modified source file',
  );
  if (dirtySource.pendingFragments().length !== 1) {
    context.fail('a rejected dirty preparation must not consume pending fragments.');
  }

  const untrackedCleanup = context.workspace({
    fragments: {
      'Fixed-20260724-100001.yaml': fragment('Fixed', 'Fixed toast exit ordering.', '2026-07-24T10:00:01.000Z'),
    },
  });
  untrackedCleanup.write('notes.txt', 'stray cleanup notes\n');
  expectFailure(
    context,
    untrackedCleanup.prepare(),
    'outside the Release Preparation contract',
    'preparing over an untracked cleanup file',
  );
  if (untrackedCleanup.exists('.changes/0.2.1.md')) {
    context.fail('a rejected dirty preparation must not create a version record.');
  }
}

function fixtureVersionDisagreementFails(context) {
  const workspace = context.workspace({
    manifestVersion: '0.1.9',
    fragments: {
      'Fixed-20260724-100001.yaml': fragment('Fixed', 'Fixed toast exit ordering.', '2026-07-24T10:00:01.000Z'),
    },
  });
  expectFailure(
    context,
    workspace.prepare(),
    'disagrees with the latest Released Version Notes record',
    'preparing from a disagreeing package version',
  );
  if (workspace.pendingFragments().length !== 1) {
    context.fail('a version disagreement must not consume pending fragments.');
  }
}

function fixtureTamperedCandidateFailsRevalidation(context) {
  const workspace = context.workspace({
    fragments: {
      'Fixed-20260724-100001.yaml': fragment('Fixed', 'Fixed toast exit ordering.', '2026-07-24T10:00:01.000Z'),
    },
  });
  if (!expectSuccess(context, workspace.prepare(), 'preparation before tampering')) return;

  workspace.write('packages/angular/package.json', '{\n  "name": "hell-ui",\n  "version": "9.9.9"\n}\n');
  const versionTamper = workspace.contractErrors();
  if (!versionTamper.some((error) => error.includes('9.9.9.md'))) {
    context.fail(
      `a package version that disagrees with the prepared record must fail re-validation; got: ${versionTamper.join(' | ') || '(no errors)'}`,
    );
  }

  workspace.write('packages/angular/package.json', '{\n  "name": "hell-ui",\n  "version": "0.2.1"\n}\n');
  workspace.append('CHANGELOG.md', '\nHand-edited aggregate line.\n');
  const aggregateTamper = workspace.contractErrors();
  if (!aggregateTamper.some((error) => error.includes('does not reproduce'))) {
    context.fail(
      `a hand-edited aggregate must fail re-validation; got: ${aggregateTamper.join(' | ') || '(no errors)'}`,
    );
  }
}

function fixtureFailedMergePreservesOutput(context) {
  const workspace = context.workspace({
    omitHeader: true,
    fragments: {
      'Breaking-20260724-100001.yaml': breakingFragment(
        'Renamed the widget input.',
        'Rename hellWidget to hellGadget in consumer templates.',
        '2026-07-24T10:00:01.000Z',
      ),
    },
  });

  const result = workspace.prepare();
  expectFailure(context, result, 'changie merge', 'preparation with a broken merge step');
  if (!result.mutated) {
    context.fail('a merge failure after batching must report the repository as mutated.');
  }
  if (!workspace.exists('.changes/0.3.0.md')) {
    context.fail('a failed preparation must preserve the batched record instead of rolling back.');
  }
  if (workspace.pendingFragments().length !== 0) {
    context.fail('a failed preparation must not silently restore consumed fragments.');
  }
  if (workspace.manifestVersion() !== '0.3.0') {
    context.fail('a failed preparation must preserve the updated package version for inspection.');
  }
}
